		Polar Alignment using a CCD Camera</strong><br />
		

		
			<p class="style6">For CCD imagers, the camera is the ideal tool for polar aligning the telescope. The ability to bin and then zoom in on the image or to obtain a sub-frame at full resolution, allows the misalignment motion of a star be detected and corrected without the strain of observing at high magnification with a reticle eyepiece. Follow the procedure below to make polar alignment easy and accurate.

			 </p>
			 <ol class="style6">
				 <li>Coarsely align to the celestial pole using two-star alignment or polar scope or by eyeball. Align well enough to ensure that your adjustments will allow polar alignment to be achieved. If you carefully level the base of your mount before beginning the drift alignment, you should only need to make one adjustment on azimuth and one on altitude. If the base of the mount is not perfectly level, then the two adjustments can slightly affect each other, requiring several iterations on each to achieve good polar alignment.</li>
				 <li>Aim at the intersection of celestial equator and the meridian. This is the point at which an error in azimuth alignment causes the maximum apparent drift in declination. 
				 </li>
				 <li>Adjust the camera rotation so that slewing the scope E/W causes no N/S change in the star's position. Move the scope's position if necessary to put a star near the bottom of the frame to make it easy to detect N/S motion as you slew the star E/W. 
				 </li>
				 <li>Pick star nearest the intersection that can be seen clearly in a 2-3 second exposure. Bin 2x2 or 3x3 if necessary to get reasonably fast update rates. A new exposure every 2-5 seconds is a good rate. Zoom in x3 or x4 to nearly fill the screen with the region around the star. This will make it much easier to observe small rates of drift.</li>
				 <li>&nbsp;In CCDSoft, you may want to use focus mode because it continuously takes and displays new images. 
