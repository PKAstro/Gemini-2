				<li> The new unit has only one power connector.</li>
				<li> The Classic hand controller jack has been eliminated.&nbsp; 
				They say the new hand control they sell has the same version of 
				the switches that is on the back of the Graphics hand control, 
				so no need for a Classic Hand Controller Jack.</li>
				<li> The DEC and RA connectors have been rotated 90 degrees, so 
				that right angle connectors if used will not interfere with 
				other connectors.</li>
				<li> The PCB has been completely redesigned, with all the latest 
				fixes applied.</li>
				<li> The front panel connectors have been totally rearranged.</li>
				<li> The Serial Port 1 now has been relabeled "Serial Port/GPS"</li>
				<li> The Serial Port 2 now has been relabeled "HC"</li>
				<li> The new unit's battery has been changed to the readily available 
				CR2450 which has a slightly higher mAH rating than the previous CR2354.
				The holder has been changed to something much less damaging to 
				one's finger nails. The cell snaps into place, and releases with a simple move. 
				See
				<a href="http://gemini-2.com/videos/Losmandy_Gemini_Battery_CR2450.mp4" target="_blank">
				changing CR2450 battery movie</a>.</li>
				<li> The Din connectors are now rock solid and do not suffer pin 
				Fracture.</li>
