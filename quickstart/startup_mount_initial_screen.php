		must start with the mount pointed to CWD position as in a cold start. If 
		you have models build, but have moved your Right Ascension axis or 
		Declination axis, but not the location of the mount itself, then you can 
		use this startup mode.<br />
		<br />
		<strong><em>Warm Restart</em></strong> - This mode also remembers your 
		modeling and all setting. You can only use this mode if, and only if you 
